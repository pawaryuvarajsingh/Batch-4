// 10. Write difference between var and let.

         // var is function-scoped, while let is block-scoped. This means that variables declared with var are accessible throughout the entire function in which they are declared, while variables declared with let are only accessible within the block (e.g., within a loop or an if statement) in which they are declared. Additionally, var allows for variable hoisting, meaning that you can use a variable before it is declared, while let does not allow for hoisting and will throw an error if you try to use a variable before it is declared.
         
